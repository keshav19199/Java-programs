package java_prog;

public class Challenge_28 {

	public static void main(String[] args) {

		int num=30;
		for(int i=1; i<=num; i++)
		{
			for(int j=1; j<=10; j++)
			{
				System.out.print(" "+i*j);
			}
			System.out.println();
			System.out.println("***********************************");
		}
		System.out.println();
	}

}
