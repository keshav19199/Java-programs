package java_prog.operations;

public class Arithmetic4 {

	public static void main(String[] args) {

		int num1=12;
		int num2=4;
		
		System.out.println(num1+num2);		
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		System.out.println(num1%num2);
		System.out.println("Done..");

	}

}
