package java_prog.operations;

public class Bitwise {
	public static void main(String[] args) {
		
		int a=60;
		int b=13;
		
		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(a^b);
		System.out.println(~a);
		System.out.println(~b);
		System.out.println(a<<2);
		System.out.println(a>>2);
		System.out.println(a>>>2);




	}

}
