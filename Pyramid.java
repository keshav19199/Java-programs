package My_Own_Programs;

public class Pyramid {

	public static void main(String[] args) {

		System.out.println("*\n* *\n* * *\n* * * *\n* * * * *");
		System.out.println();
		System.out.println("* * * * *\n* * * *\n* * *\n* *\n*");
		System.out.println();
		System.out.println("        *\n      * *\n    * * *\n  * * * *\n* * * * *");
		
	}

}
